#--> odds or evens , modulo operator

num = input("Enter a #, and i'll tell you if it's odd or even: ")
num = int(num)

if num % 2 == 0:
	print("\n" + str(num) + " is an EVEN #.")
else:
	print("\n" + str(num) + " is an ODD #.")

	
